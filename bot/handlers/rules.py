"""Команда /rules — гайд по Бурмалде (экономика, рынки, Mini App)."""
import html

from aiogram import Router, types
from aiogram.filters import Command

from common.logger.logger import get_logger

logger = get_logger(__name__)
router = Router()


RULES_TEXT = """БУРМАЛДА — экономика и ставки чата

КАК ЭТО РАБОТАЕТ
В каждом чате — свой банк и свои гривны («бурмалда-гривны»).
Новичок получает 1000 гривен при первом действии в боте.
Все операции записываются в журнал транзакций.

ЗАРАБОТОК
• /fag — ежедневный розыгрыш «кто_дня». Победитель получает приз из банка чата.
• Победа на рынках — выплата из общего пула.
• /transfer @user 100 — перевод другому участнику.

БАЗОВЫЕ КОМАНДЫ
/balance              — твой баланс
/leaderboard          — топ-20 чата
/economy              — состояние банка
/transfer @user СУММА — перевод

РЫНКИ СТАВОК (parimutuel)
Принцип: все ставки идут в общий пул. После закрытия рынка пул делится между поставившими на выигравший вариант — пропорционально размеру их ставок. Комиссия 5% уходит в банк чата.

/markets                   — список открытых рынков
/market ID                 — карточка рынка
/bet ID ВАРИАНТ СУММА      — поставить (минимум 10 гривен)
/portfolio                 — мои ставки

СОЗДАНИЕ РЫНКОВ
/market_create — диалог: вопрос, варианты, дата закрытия. Комиссия за создание — 100 гривен в банк чата.
/market_import URL — импорт рынка с Polymarket или Manifold. Резолвится автоматически когда внешний рынок закрывается.

MINI APP
/casino — открывает Mini App с балансом, рынками, портфолио и лидербордом в удобном UI.
Все то же самое доступно командами — Mini App это просто красивая обёртка.

ВАЖНО
• Минимальная ставка: 10 гривен.
• Если рынок отменён — деньги возвращаются.
• Создание рынка и комиссии за ставки уходят в банк чата, который потом распределяется через номинации."""


@router.message(Command("rules"))
async def cmd_rules(message: types.Message):
    safe = html.escape(RULES_TEXT)
    await message.answer(f"<pre>{safe}</pre>", parse_mode="HTML")
